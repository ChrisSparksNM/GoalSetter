    <script>
        // Calendar functionality
        let currentDate = new Date();
        let currentMonth = currentDate.getMonth();
        let currentYear = currentDate.getFullYear();
        
        // Goals data for calendar
        @php
            $currentMonthGoals = Auth::user()->goals()
                ->whereMonth('end_date', date('m'))
                ->whereYear('end_date', date('Y'))
                ->get()
                ->map(function($goal) {
                    return [
                        'id' => $goal->id,
                        'title' => $goal->title,
                        'date' => $goal->end_date->format('Y-m-d'),
                        'status' => $goal->status,
                        'is_recurring' => $goal->is_recurring ?? false,
                        'recurrence_display' => $goal->recurrence_display ?? 'One-time'
                    ];
                });
        @endphp
        const goals = @json($currentMonthGoals);
        
        function generateCalendar(month, year) {
            const monthNames = [
                "January", "February", "March", "April", "May", "June",
                "July", "August", "September", "October", "November", "December"
            ];
            
            const daysInMonth = new Date(year, month + 1, 0).getDate();
            const firstDay = new Date(year, month, 1).getDay();
            
            let calendarHTML = `
                <div class="text-center mb-4">
                    <h4 class="text-lg font-semibold text-gray-900">${monthNames[month]} ${year}</h4>
                </div>
                <div class="grid grid-cols-7 gap-1 mb-2">
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Sun</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Mon</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Tue</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Wed</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Thu</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Fri</div>
                    <div class="text-center text-xs font-medium text-gray-500 py-2">Sat</div>
                </div>
                <div class="grid grid-cols-7 gap-1">
            `;
            
            // Empty cells for days before the first day of the month
            for (let i = 0; i < firstDay; i++) {
                calendarHTML += '<div class="h-20 border border-gray-200 bg-gray-50"></div>';
            }
            
            // Days of the month
            for (let day = 1; day <= daysInMonth; day++) {
                const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
                const dayGoals = goals.filter(goal => goal.date === dateStr);
                const isToday = dateStr === new Date().toISOString().split('T')[0];
                
                let dayClass = 'h-20 border border-gray-200 bg-white p-1 cursor-pointer hover:bg-gray-50';
                if (isToday) {
                    dayClass += ' ring-2 ring-blue-500';
                }
                
                calendarHTML += `
                    <div class="${dayClass}" onclick="selectDate('${dateStr}')">
                        <div class="text-sm font-medium text-gray-900">${day}</div>
                        <div class="mt-1 space-y-1">
                `;
                
                dayGoals.forEach(goal => {
                    const statusColor = goal.status === 'completed' ? 'bg-green-100 text-green-800' : 
                                       goal.status === 'active' ? 'bg-blue-100 text-blue-800' : 'bg-gray-100 text-gray-800';
                    calendarHTML += `
                        <div class="text-xs px-1 py-0.5 rounded ${statusColor} truncate" title="${goal.title}">
                            ${goal.title.substring(0, 15)}${goal.title.length > 15 ? '...' : ''}
                            ${goal.is_recurring ? ' ðŸ”„' : ''}
                        </div>
                    `;
                });
                
                calendarHTML += '</div></div>';
            }
            
            calendarHTML += '</div>';
            document.getElementById('calendar').innerHTML = calendarHTML;
        }
        
        function previousMonth() {
            currentMonth--;
            if (currentMonth < 0) {
                currentMonth = 11;
                currentYear--;
            }
            generateCalendar(currentMonth, currentYear);
        }
        
        function nextMonth() {
            currentMonth++;
            if (currentMonth > 11) {
                currentMonth = 0;
                currentYear++;
            }
            generateCalendar(currentMonth, currentYear);
        }
        
        function selectDate(dateStr) {
            document.getElementById('end_date').value = dateStr;
            document.getElementById('start_date').value = dateStr;
        }
        
        function toggleRecurringOptions() {
            const recurrenceType = document.getElementById('recurrence_type').value;
            const singleDateField = document.getElementById('singleDateField');
            const recurringDateFields = document.getElementById('recurringDateFields');
            
            if (recurrenceType === 'none') {
                singleDateField.classList.remove('hidden');
                recurringDateFields.classList.add('hidden');
                document.getElementById('end_date').required = true;
                document.getElementById('start_date').required = false;
            } else {
                singleDateField.classList.add('hidden');
                recurringDateFields.classList.remove('hidden');
                document.getElementById('end_date').required = false;
                document.getElementById('start_date').required = true;
            }
        }
        
        // Video modal functions
        function showSmartGoalsVideo() {
            const modal = document.getElementById('videoModal');
            const iframe = document.getElementById('smartGoalsVideo');
            
            iframe.src = 'https://www.youtube.com/embed/1-SvuFIQjK8';
            modal.classList.remove('hidden');
        }
        
        function closeVideoModal() {
            const modal = document.getElementById('videoModal');
            const iframe = document.getElementById('smartGoalsVideo');
            
            modal.classList.add('hidden');
            iframe.src = '';
        }
        
        // Initialize calendar on page load
        document.addEventListener('DOMContentLoaded', function() {
            generateCalendar(currentMonth, currentYear);
        });
        
        // Modal event listeners
        document.getElementById('videoModal').addEventListener('click', function(e) {
            if (e.target === this) {
                closeVideoModal();
            }
        });
        
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape') {
                closeVideoModal();
            }
        });
    </script>
